public class Add_Subtract {

    public int add(int a, int b) {
        return a + b;
    }

    public double add(double c, double d) {
        return c + d;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public double subtract(double c, double d) {
        return c - d;
    }
}
