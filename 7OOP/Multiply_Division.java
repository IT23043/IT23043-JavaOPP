public class Multiply_Division extends Add_Subtract {
    public int multiply(int a, int b) {
        return a * b;
    }

    public double multiply(double c, double d) {
        return c * d;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    public double divide(double c, double d) {
        return c / d;
    }
}
